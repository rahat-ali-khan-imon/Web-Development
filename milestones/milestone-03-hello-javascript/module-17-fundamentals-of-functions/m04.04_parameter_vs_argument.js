// Parameter vs Argument

// ✅ Parameter --->> Function declaration এর ভিতরের variable
function greet(name) { }
// 👉 এখানে name হলো parameter


// ✅ Argument --->> Function call করার সময় যে value পাঠাই
greet("Rahim");
// 👉 "Rahim" হলো argument


/*
Simple Formula:
Parameter = Variable in function definition
Argument = Actual value during function call
*/