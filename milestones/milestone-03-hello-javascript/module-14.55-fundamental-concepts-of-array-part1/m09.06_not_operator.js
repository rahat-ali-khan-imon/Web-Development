// Boolean Conversion Examples

console.log(0);
console.log(!0);
console.log(!!0);        // false
console.log();

console.log(1);
console.log(!1);
console.log(!!1);        // true
console.log();

console.log(!"");
console.log(!!"");       // false
console.log();

console.log("Hi");
console.log(!"Hi");
console.log(!!"Hi");     // true
console.log();

console.log(null);
console.log(!null);
console.log(!!null);     // false
console.log();

console.log(undefined);
console.log(!undefined);
console.log(!!undefined);// false